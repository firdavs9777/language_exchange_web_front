let updated = 0;
for (const [file, key] of Object.entries(FILE_TO_KEY)) {
  const fp = path.join(localesDir, file);
  const json = JSON.parse(fs.readFileSync(fp, "utf8"));
  if (!json.chatPage) {
    console.warn(`SKIP ${file}: no chatPage root`);
    continue;
  }
  const t = T[key];
  // top-level chatPage keys
  json.chatPage.backToConversations = t.backToConversations;
  json.chatPage.moreOptions = t.moreOptions;
  json.chatPage.retrySending = t.retrySending;
  json.chatPage.input = t.input;
  json.chatPage.list = t.list;
  json.chatPage.deleteModal = t.deleteModal;
  json.chatPage.gif = t.gif;
  // features.translation lives under chatPage.features
  json.chatPage.features = json.chatPage.features || {};
  json.chatPage.features.translation = t.translation;
  fs.writeFileSync(fp, JSON.stringify(json, null, 2) + "\n", "utf8");
  updated += 1;
  console.log(`✓ ${file}`);
}
console.log(`Updated ${updated} locale files.`);
